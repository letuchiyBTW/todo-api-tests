"""
Автотесты для публичного API JSONPlaceholder
Сайт: https://jsonplaceholder.typicode.com/
Это учебное API для тренировки тестирования
"""

import pytest
import requests
import time

# Базовый адрес API (все запросы будут начинаться с этого)
BASE_URL = "https://jsonplaceholder.typicode.com"

# -------------------------------------------------------------------
# ТЕСТ 1: Получение всех постов
# -------------------------------------------------------------------

def test_get_all_posts():
    """
    Проверяем, что можно получить список всех постов
    Ожидаем: статус 200, возвращается список, в списке есть элементы
    """
    
    # Отправляем GET-запрос на адрес /posts
    response = requests.get(f"{BASE_URL}/posts")
    
    # Проверяем, что статус ответа 200 (OK)
    # Если статус другой - тест упадет с ошибкой
    assert response.status_code == 200, f"Ожидался статус 200, получен {response.status_code}"
    
    # Преобразуем ответ из JSON в Python-объект (список)
    data = response.json()
    
    # Проверяем, что вернулся именно список
    assert isinstance(data, list), "Ответ должен быть списком"
    
    # Проверяем, что список не пустой (хотя бы один пост есть)
    assert len(data) > 0, "Список постов пуст"
    
    # Берем первый пост из списка
    first_post = data[0]
    
    # Проверяем, что у поста есть все нужные поля
    assert "id" in first_post, "У поста нет поля id"
    assert "title" in first_post, "У поста нет поля title"
    assert "body" in first_post, "У поста нет поля body"
    assert "userId" in first_post, "У поста нет поля userId"
    
    # Если дошли до сюда - тест пройден
    print("Тест test_get_all_posts пройден успешно")

