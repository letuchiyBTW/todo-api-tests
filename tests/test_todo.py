# ТЕСТ 8: Негативные тесты с невалидными ID (ИСПРАВЛЕННАЯ ВЕРСИЯ)
# -------------------------------------------------------------------

@pytest.mark.parametrize("invalid_id", [
    -1,      # отрицательное число
    0,       # ноль
    "abc",   # строка вместо числа
    1.5,     # дробное число
    999999,  # слишком большое число
    "",      # пустая строка
    None     # ничего
])
def test_invalid_post_ids(invalid_id):
    """
    Проверяем, как API реагирует на некорректные ID
    """
    
    # Особый случай: пустая строка
    if invalid_id == "":
        # Для пустой строки API возвращает весь список (200)
        # Это не ошибка, просто особенность API
        response = requests.get(f"{BASE_URL}/posts/{invalid_id}")
        assert response.status_code == 200
        print(f"Пустой ID обработан как запрос всего списка")
        return
    
    # Особый случай: None
    if invalid_id is None:
        # Для None API тоже может вернуть список
        response = requests.get(f"{BASE_URL}/posts/{invalid_id}")
        # Принимаем оба варианта
        if response.status_code == 200:
            print(f"None обработан как запрос всего списка")
            return
    
    # Для всех остальных некорректных ID
    response = requests.get(f"{BASE_URL}/posts/{invalid_id}")
    
    # Проверяем, что вернулась ошибка
    assert response.status_code in [400, 404, 422], \
        f"Для ID {invalid_id} ожидалась ошибка, получен статус {response.status_code}"

