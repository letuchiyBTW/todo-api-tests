# ТЕСТ 4: Создание нового поста
# -------------------------------------------------------------------

def test_create_post():
    """
    Проверяем, что можно создать новый пост через POST-запрос
    Ожидаем: статус 201 Created, сервер возвращает созданный пост с ID
    """
    
    # Данные для нового поста (словарь)
    new_post = {
        "title": "Мой тестовый пост",
        "body": "Этот пост создан автоматическим тестом",
        "userId": 1
    }
    
    # Отправляем POST-запрос с данными
    # json=new_post означает, что мы передаем данные в формате JSON
    response = requests.post(
        f"{BASE_URL}/posts",
        json=new_post
    )
    
    # Проверяем статус (201 = Created)
    assert response.status_code == 201, f"Ожидался статус 201, получен {response.status_code}"
    
    # Получаем ответ
    data = response.json()
    
    # Проверяем, что сервер вернул наши данные
    assert data["title"] == new_post["title"], "Заголовок не совпадает"
    assert data["body"] == new_post["body"], "Текст не совпадает"
    assert data["userId"] == new_post["userId"], "userId не совпадает"
    
    # Проверяем, что сервер присвоил ID новому посту
    assert "id" in data, "Сервер не вернул ID"
    assert isinstance(data["id"], int), "ID должен быть числом"
    
    print(f"Тест test_create_post пройден успешно, создан пост с ID={data['id']}")

