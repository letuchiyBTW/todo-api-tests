def test_response_headers():
    """
    Проверяем, что сервер возвращает правильные заголовки
    """
    
    response = requests.get(f"{BASE_URL}/posts/1")
    
    # Проверяем, что сервер говорит, что это JSON
    assert "application/json" in response.headers.get("Content-Type", ""), \
        "Сервер должен возвращать JSON"
    
    # Проверяем, что сервер существует (не скрывает свою идентичность)
    assert "Server" in response.headers, "Сервер не указал имя в заголовках"
    
    print("Заголовки ответа корректны")

