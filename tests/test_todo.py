# -------------------------------------------------------------------

def test_update_post():
    """
    Проверяем, что можно обновить существующий пост через PUT-запрос
    Ожидаем: статус 200, данные обновляются
    """
    
    # Данные для обновления
    updated_data = {
        "id": 1,
        "title": "Обновленный заголовок",
        "body": "Этот текст был обновлен тестом",
        "userId": 1
    }
    
    # Отправляем PUT-запрос (полное обновление)
    response = requests.put(
        f"{BASE_URL}/posts/1",
        json=updated_data
    )
    
    # Проверяем статус
    assert response.status_code == 200, f"Ожидался статус 200, получен {response.status_code}"
    
    # Получаем ответ
    data = response.json()
    
    # Проверяем, что данные обновились
    assert data["title"] == updated_data["title"]
    assert data["body"] == updated_data["body"]
    
    print("Тест test_update_post пройден успешно")

