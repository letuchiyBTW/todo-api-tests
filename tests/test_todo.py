# -------------------------------------------------------------------

def test_response_time():
    """
    Проверяем, что сервер отвечает достаточно быстро
    """
    
    # Засекаем время
    start_time = time.time()
    
    # Делаем запрос
    response = requests.get(f"{BASE_URL}/posts")
    
    # Считаем, сколько прошло времени
    end_time = time.time()
    response_time = end_time - start_time
    
    # Проверяем, что ответ пришел быстрее 2 секунд
    assert response_time < 2, f"Сервер отвечал слишком долго: {response_time:.2f} сек"
    
    print(f"Время ответа: {response_time:.2f} сек (норма)")